import { existsSync, mkdirSync, readdirSync, statSync, writeFileSync } from 'node:fs';
import { dirname, extname, join, resolve } from 'node:path';
import { execFileSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const projectRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const wantThisDir = join(projectRoot, 'assets', 'want-this');
const optimizedDir = join(wantThisDir, 'optimized');
const manifestPath = join(wantThisDir, 'manifest.js');

const supportedImageExts = new Set(['.png', '.jpg', '.jpeg', '.webp', '.avif']);
const supportedVideoExts = new Set(['.mp4']);

function run(command, args) {
  execFileSync(command, args, { stdio: 'inherit' });
}

function sanitizeBaseName(value, fallbackIndex) {
  const normalized = value
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');

  return normalized || `item-${String(fallbackIndex + 1).padStart(2, '0')}`;
}

function isFreshEnough(inputPath, outputPath) {
  if (!existsSync(outputPath)) return false;

  const outputMtime = statSync(outputPath).mtimeMs;
  const inputMtime = statSync(inputPath).mtimeMs;
  return outputMtime >= inputMtime;
}

function ensureFreshImage(inputPath, outputPath) {
  if (isFreshEnough(inputPath, outputPath)) return false;

  const scaleFilter = "scale='if(gt(iw,1600),1600,iw)':-2:flags=lanczos";

  run('ffmpeg', [
    '-y',
    '-hide_banner',
    '-loglevel',
    'error',
    '-i',
    inputPath,
    '-vf',
    scaleFilter,
    '-frames:v',
    '1',
    '-c:v',
    'libaom-av1',
    '-still-picture',
    '1',
    '-cpu-used',
    '6',
    '-crf',
    '28',
    '-b:v',
    '0',
    '-pix_fmt',
    'yuv420p',
    outputPath,
  ]);

  return true;
}

function ensureFreshVideo(inputPath, outputPath) {
  if (isFreshEnough(inputPath, outputPath)) return false;

  const scaleFilter = "scale='if(gt(iw,1600),1600,iw)':-2:flags=lanczos";

  run('ffmpeg', [
    '-y',
    '-hide_banner',
    '-loglevel',
    'error',
    '-i',
    inputPath,
    '-vf',
    scaleFilter,
    '-an',
    '-c:v',
    'libx264',
    '-preset',
    'slow',
    '-crf',
    '20',
    '-pix_fmt',
    'yuv420p',
    '-movflags',
    '+faststart',
    outputPath,
  ]);

  return true;
}

function ensureFreshPoster(inputPath, outputPath) {
  if (isFreshEnough(inputPath, outputPath)) return false;

  const scaleFilter = "scale='if(gt(iw,1600),1600,iw)':-2:flags=lanczos";

  run('ffmpeg', [
    '-y',
    '-hide_banner',
    '-loglevel',
    'error',
    '-i',
    inputPath,
    '-vf',
    scaleFilter,
    '-frames:v',
    '1',
    '-c:v',
    'libaom-av1',
    '-still-picture',
    '1',
    '-cpu-used',
    '6',
    '-crf',
    '28',
    '-b:v',
    '0',
    '-pix_fmt',
    'yuv420p',
    outputPath,
  ]);

  return true;
}

function toPublicPath(fileName) {
  return `/assets/want-this/optimized/${fileName}`;
}

function buildManifest(entries) {
  const serialized = JSON.stringify(entries, null, 2);

  return [
    '/* Auto-generated by scripts/prepare-want-this-assets.mjs */',
    `window.atelaWantThisGallery = Object.freeze(${serialized});`,
    '',
  ].join('\n');
}

function main() {
  mkdirSync(wantThisDir, { recursive: true });
  mkdirSync(optimizedDir, { recursive: true });

  const rawFiles = readdirSync(wantThisDir, { withFileTypes: true })
    .filter((entry) => entry.isFile() && !entry.name.startsWith('.'))
    .map((entry) => entry.name)
    .filter((fileName) => {
      const ext = extname(fileName).toLowerCase();
      return supportedImageExts.has(ext) || supportedVideoExts.has(ext);
    })
    .sort((left, right) => left.localeCompare(right, 'en'));

  const usedSlugs = new Set();
  const manifestEntries = rawFiles.map((fileName, index) => {
    const inputPath = join(wantThisDir, fileName);
    const ext = extname(fileName).toLowerCase();
    const baseName = fileName.slice(0, -ext.length);
    let slug = sanitizeBaseName(baseName, index);

    while (usedSlugs.has(slug)) {
      slug = `${slug}-${String(index + 1).padStart(2, '0')}`;
    }
    usedSlugs.add(slug);

    if (supportedVideoExts.has(ext)) {
      const videoOutput = `${slug}.mp4`;
      const posterOutput = `${slug}.poster.avif`;
      const videoChanged = ensureFreshVideo(inputPath, join(optimizedDir, videoOutput));
      const posterChanged = ensureFreshPoster(inputPath, join(optimizedDir, posterOutput));

      console.log(
        `[want-this] ${fileName} -> ${videoOutput}${videoChanged ? ' (updated)' : ' (cached)'}`
      );
      console.log(
        `[want-this] ${fileName} -> ${posterOutput}${posterChanged ? ' (updated)' : ' (cached)'}`
      );

      return {
        id: slug,
        type: 'video',
        src: toPublicPath(videoOutput),
        poster: toPublicPath(posterOutput),
        alt: `ATELA want-this gallery video ${index + 1}`,
      };
    }

    const imageOutput = `${slug}.avif`;
    const imageChanged = ensureFreshImage(inputPath, join(optimizedDir, imageOutput));

    console.log(
      `[want-this] ${fileName} -> ${imageOutput}${imageChanged ? ' (updated)' : ' (cached)'}`
    );

    return {
      id: slug,
      type: 'image',
      src: toPublicPath(imageOutput),
      alt: `ATELA want-this gallery image ${index + 1}`,
    };
  });

  writeFileSync(manifestPath, buildManifest(manifestEntries));
  console.log(`[want-this] wrote manifest with ${manifestEntries.length} asset(s)`);
}

main();
